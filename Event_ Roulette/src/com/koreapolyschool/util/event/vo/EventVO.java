package com.koreapolyschool.util.event.vo;

public class EventVO {

	private String id;
	private String name;
	private String edu_state;
	private String campus;
	private String product;
	private String event_date;
	private String event_msg;
	private String event_yn;
	private String user_addr;
	private String phone;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEdu_state() {
		return edu_state;
	}

	public void setEdu_state(String edu_state) {
		this.edu_state = edu_state;
	}

	public String getCampus() {
		return campus;
	}

	public void setCampus(String campus) {
		this.campus = campus;
	}

	public String getProduct() {
		return product;
	}

	public void setProduct(String product) {
		this.product = product;
	}

	public String getEvent_date() {
		return event_date;
	}

	public void setEvent_date(String event_date) {
		this.event_date = event_date;
	}

	public String getEvent_msg() {
		return event_msg;
	}

	public void setEvent_msg(String event_msg) {
		this.event_msg = event_msg;
	}

	public String getEvent_yn() {
		return event_yn;
	}

	public void setEvent_yn(String event_yn) {
		this.event_yn = event_yn;
	}

	public String getUser_addr() {
		return user_addr;
	}

	public void setUser_addr(String user_addr) {
		this.user_addr = user_addr;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

}
